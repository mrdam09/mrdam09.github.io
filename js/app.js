function navigation(){
	var navi_class = 'navigation';
	var navi = $('.' + navi_class);
	var item_backdrop = 'navi-backdrop';

	var navi_icon = navi.parent().prepend('<div class="animated-arrow"><span></span></div>');
	navi_icon = $('.animated-arrow');
	navi.parent().prepend('<div class="'+item_backdrop+'"></div>');
	var backdrop = $('.'+item_backdrop);
	backdrop.hide(); 

	check_sub(); 
	add_logo();
	

	navi_icon.click(function(){ 
		var this_click = $(this);
		add_open(this_click);
		add_open(navi);
		add_backdrop(); 
	});

	backdrop.click(function(){
		add_open(navi_icon);
		add_open(navi);
		add_backdrop();
	});	
	// add navi_icon .open
	function add_open(this_click){
		if (!this_click.hasClass('open')){
			this_click.addClass('open');
			$('body').addClass('navi-open'); 
		}
		else if (this_click.hasClass('open')) {
			this_click.removeClass('open'); 
			$('body').removeClass('navi-open'); 
		}
		
	}
	// add backdrop
	function add_backdrop(){ 
		var check_backdrop = navi.parent().find(item_backdrop);  

		if (navi_icon.hasClass('open')){
			backdrop.show();
		}
		else if(!navi_icon.hasClass('open')){ 
			backdrop.hide();
		}
	}	
	// check has child submenu
	function check_sub(){
		var submenu = $('.'+navi_class + ' ul li').has('ul');
		var icon = 'sub-icon';
		submenu.addClass('hasSub');
		submenu.append('<span class="'+icon+'">+</span>')
		// $('.hasSub > ul').addClass('animated slideInDown');
		var sub_icon = $('.'+icon); 

		sub_icon.click(function(){
			if ($(this).hasClass('on')){
				$(this).removeClass('on');
				$(this).text('+');
				$(this).parent().removeClass('hasSub_on');
			}
			else if (!$(this).hasClass('on')){
				$(this).addClass('on');
				$(this).text('-');
				$(this).parent().addClass('hasSub_on');
			}
		});
	} 
	//Add mobile logo on navigation
	function add_logo() { 	
		var breakpoint = 991;
		var place_logo = $('.' + navi_class + ' > ul > li:first-child > a');
		var place_text = place_logo.text();

		logo_add();	
		$(window).resize(function(){
			logo_add(); 
			// console.log('thÃªm logo');
		});

		function logo_add() {
			if (($(window).width()<breakpoint)){				
				place_logo.html('<img class="navigation_brand" src="images/logo.png" alt ="logo"/>');
				// console.log($(window).width());
			}
			else if ($(window).width()>breakpoint){
				place_logo.html(place_text);
				// console.log($(window).width());
			}
		}
	}
	
}

